/// {@template screen_recorder_status}
/// A status which indicates whether the screen recorder is on or off.
/// {@endtemplate}
enum ScreenRecorderStatus {
  /// {@macro screen_recorder_status}
  ///
  /// [ScreenRecorderStatus.recorderOn] indicates that the screen recorder is on.
  recorderOn,

  /// {@macro screen_recorder_status}
  ///
  /// [ScreenRecorderStatus.recorderOff] indicates that the screen recorder is off.
  recorderOff,
}
